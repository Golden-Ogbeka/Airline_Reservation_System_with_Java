    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String departure=datepick1.getDate().toString();
        String arrival=datepick2.getDate().toString();
        String airline=cmbAirline.getSelectedItem().toString().trim();
        String destination=cmbDestination.getSelectedItem().toString().trim();
        if(departure.isEmpty()||arrival.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please all entries must be filled");
        }
        else{
        try{
            Connection con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinebase", "root", "");
            Statement st=con.createStatement();
            String sql="SELECT * FROM availableflight";
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
                //for date
                String flightNo=rs.getString("flightno");
                String price=rs.getString("price");
                String departureStored=rs.getDate("departure").toString().trim();
                String arrivalStored=rs.getDate("arrival").toString().trim();
                String destinationStored=rs.getString("destination").trim();
                String airlineStored=rs.getString("airline").trim();
                if((departureStored.equals(departure)&&arrivalStored.equals(arrival))||(destinationStored.equals(destination)&&airlineStored.equals(airline))){
                    String sql2="INSERT INTO searchavailable VALUES('"+flightNo+"','"+destinationStored+"','"+departureStored+"','"+arrivalStored+"','"+airlineStored+"','"+price+"')";
                    st.executeUpdate(sql2);
                    this.dispose();
                    Details obj=new Details();
                    obj.setSize(1000,680);
                    obj.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Sorry there are no available flights for your selection");
                }
                con.close();
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

